# A 'for' loop is used to iterate over a sequence of items (like a range of numbers)

for i in range(5):  # range(5) generates a sequence of numbers: 0, 1, 2, 3, 4
    print(i)  # This prints each number in the sequence, one at a time

# A 'while' loop repeats as long as a condition is true

counter = 0  # Starting point for the counter

while counter < 5:  # While the counter is less than 5, the loop continues
    print(counter)  # Prints the current value of counter
    counter += 1  # Increases the value of counter by 1 each time
