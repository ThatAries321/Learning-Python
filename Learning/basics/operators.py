# Performing basic arithmetic operations

x = 10  # Assigning 10 to x
y = 5  # Assigning 5 to y

# Addition
print(x + y)  # The sum of 10 and 5 is 15

# Subtraction
print(x - y)  # The result of 10 minus 5 is 5

# Multiplication
print(x * y)  # The result of 10 multiplied by 5 is 50

# Division
print(x / y)  # The result of 10 divided by 5 is 2.0 (float division)

# Modulo (remainder)
print(x % y)  # The remainder when 10 is divided by 5 is 0

# Exponentiation
print(x ** y)  # 10 raised to the power of 5 is 100000
