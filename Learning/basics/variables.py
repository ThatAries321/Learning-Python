# Assigning different data types to variables

name = "Alice"  # String: text enclosed in quotes
age = 25  # Integer: whole number without decimal points
height = 5.6  # Float: number with a decimal point
is_student = True  # Boolean: represents True or False

# Printing the values of the variables
print(name)  # This will output the value of the 'name' variable: Alice
print(age)  # This will output the value of the 'age' variable: 25
print(height)  # This will output the value of the 'height' variable: 5.6
print(is_student)  # This will output the value of the 'is_student' variable: True
